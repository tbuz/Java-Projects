//Eine Klasse wird definiert:
public class StringVerkettung {
	//Der Konstruktor wird definiert:
	public static void main(String[] args){
		
		//Drei Strings werden deklariert und initialisiert:
		String a = "Unsere Gruppe lernt viel besser, ";
		String b = "wenn sie sich nicht nur im Tutorium ";
		String c = "zur Bearbeitung der Hausaufgaben trifft.";
		
		//Strings werden als result zusammengefügt:
		String result = a+b+c ;
		
		//result wird auf dem Bildschirm ausgegeben:
		System.out.println(result);
		
	}	//Ende des Konstruktors

} 		//Ende der Klasse
